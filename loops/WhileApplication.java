import java.util.Scanner;

// Demonstrate while, do-while, for, for-each loops
public class WhileApplication {
    public static void main(String[] args) {
        System.out.println("Welcome to While Loop");
        int i = 10;
        while (i<=20){
            System.out.printf("%d *\n",i);
            i++;
       }
    }
}
